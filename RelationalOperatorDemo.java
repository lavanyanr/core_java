class RelationalOperatorDemo{

public static void main(String[] args){
  
        int i = 7;
        int j = 11;
        boolean result = i==j;
        System.out.println("i==j:"+result);
        boolean result1 = i!=j;
        System.out.println("i!=j:"+result1);
        boolean result2 = i>j;
        System.out.println("i>j:"+result2);
        boolean result3 = i<j;
        System.out.println("i<j:"+result3);
        boolean result4 = i>=j;
        System.out.println("i>=j:"+result4);
        boolean result5 = i<=j;
        System.out.println("i<=j:"+result5);
}
}